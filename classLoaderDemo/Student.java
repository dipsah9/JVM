package classLoaderDemo;

public class Student {
    String name;
    int roll_no;

    public void setName(String name ){
        this.name = name;
    }

    public String getName(){
        return this.name;
    }

    public void setRollNo(int no){
        this.roll_no = no;
    }

    public int getRollNo(){
        return this.roll_no;
    }
}
